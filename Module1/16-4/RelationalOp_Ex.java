package com.a164;

// < , > , <=, >=, == , !=

public class RelationalOp_Ex 
{
	public static void main(String[] args) 
	{
		int a = 6;
		int b = 3;
		int c = 6;
		
		System.out.println(a<b);
		System.out.println(a>b);
		System.out.println(a<=c);
		System.out.println(a>=b);
		System.out.println(a==b);
		System.out.println(a!=b);
	}
}
